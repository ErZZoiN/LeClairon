    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String formatpays = (String) combo_pays.getSelectedItem();
        switch(formatpays)
        {
            case "France" : mw.td.setFormatLocale(Locale.FRENCH);break;
            case "Royaume Uni" : mw.td.setFormatLocale(Locale.UK);break; 
            case "Italie" : mw.td.setFormatLocale(Locale.ITALY);break;
            case "U.S.A." : mw.td.setFormatLocale(Locale.US);break;
        }
        String formatheure = (String) combo_date.getSelectedItem();
        switch(formatheure)
        {
            case "Short": mw.td.setFormatHeure(DateFormat.SHORT);break;
            case "Medium": mw.td.setFormatHeure(DateFormat.MEDIUM);break;
            case "Full": mw.td.setFormatHeure(DateFormat.FULL);break;
        }
        String formatdate = (String) combo_heure.getSelectedItem();
        switch(formatdate)
        {
            case "Short": mw.td.setFormatDate(DateFormat.SHORT);break;
            case "Medium": mw.td.setFormatDate(DateFormat.MEDIUM);break;
            case "Full": mw.td.setFormatDate(DateFormat.FULL);break;
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

