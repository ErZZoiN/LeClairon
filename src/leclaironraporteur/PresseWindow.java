    @Override
    public void NotifyNewsDetected(NotifyNewsEvent e)
    {
        try {
            TimeUnit.MILLISECONDS.sleep(10); // Exécution trop rapide sans un délais, on lit "RIEN"
        } catch (InterruptedException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }

        String message = getBasicServer().getMessage();
        if(message.equals("RIEN"))
        {
            return;
        }
        else
        {
            label_rep.setText(message);
            addLog("Confirmation reçue ! ");
        }
        
    }
    
    public void updateTable()
    {
        for(int i=0;i<getTaille();i++) // On vide la JTable
        {
            for(int j=0;j<4;j++)
            {
                Table_News.setValueAt("", i, j);
            }
        }
        
        for(int i=0;i<getListeNews().size();i++) // On remplit la JTable avec notre ArrayList de news
        {
            News tmp = getListeNews().get(i);
            
            Table_News.getModel().setValueAt(tmp.getTitre(), i, 0);
            Table_News.getModel().setValueAt(tmp.getCategorie().getLibelle(), i, 1);
            
            if(tmp.isImportant())
                Table_News.getModel().setValueAt("Y", i, 2);
            else
                Table_News.getModel().setValueAt("F", i, 2);
            
            Table_News.getModel().setValueAt(tmp.getnomJournaliste(), i, 3);
        }        
    }
    
    public void addLog(String s)
    {
        Date date = new Date();
        Calendar calendar = GregorianCalendar.getInstance();
        calendar.setTime(date);
        
        String log = "["+calendar.get(Calendar.HOUR_OF_DAY)+":"+calendar.get(Calendar.MINUTE)+"] "+s; // Heure:Minute + Message
        
        textAreaLog.append(log+System.getProperty("line.separator")); // Ira à la ligne peu importe l'OS
    }
    
