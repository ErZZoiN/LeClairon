    private void buttonEnreistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEnreistrerActionPerformed
        

        if(buttonGroup.getSelection() != null && !TF_Titre.getText().equals("") && !TF_Journaliste.getText().equals(""))
        {
            News news = new News();
            news.setTitre(TF_Titre.getText());
            news.setText(TF_Texte.getText());
            news.setnomJournaliste(TF_Journaliste.getText());

            // On définit la catégorie selon le radiobutton sélectionné
            if(RB_Inter.isSelected())news.setCategorie(Categorie.INTERNATIONAL);
            else if(RB_Pol.isSelected())news.setCategorie(Categorie.POLITIQUE);
            else if(RB_Sports.isSelected())news.setCategorie(Categorie.SPORT);
            else if(RB_Ragots.isSelected())news.setCategorie(Categorie.RAGOT);


            news.setImportant(CB_Important.isSelected());

            getListeNews().add(news);

            TF_Titre.setText("");
            TF_Texte.setText("");
            TF_Journaliste.setText("");
            buttonGroup.clearSelection();
            CB_Important.setSelected(false);

            addLog("News enregistree avec succes ! ("+news.getnomJournaliste()+")");

            updateTable();      
        }
        
        
    }//GEN-LAST:event_buttonEnreistrerActionPerformed

