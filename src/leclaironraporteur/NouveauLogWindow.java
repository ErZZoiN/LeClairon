    private void validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerActionPerformed
        try{
            verifPassword(String.valueOf(motDePasse.getPassword()), String.valueOf(motDePasse2.getPassword()));
            FileOutputStream out = new FileOutputStream("config.properties");
            getFenprin().getLogins().put(String.valueOf(motDePasse.getPassword()), login.getText());
            getFenprin().getLogins().store(out, "C'est mes logins lolilol");
            this.dispose();
        }
        catch(LoginException e)
        {
            e.display();
            motDePasse.setText("");
            motDePasse2.setText("");
        }
        catch(FileNotFoundException e)
        {
            new JDialog(this, "BUGBUGBUG").setVisible(true);
        }
        catch (IOException e)
        {
            new JDialog(this, "BUGBUGBUG").setVisible(true);
        }
    }//GEN-LAST:event_validerActionPerformed

