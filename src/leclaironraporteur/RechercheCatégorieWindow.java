    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        DefaultListModel dlm = new DefaultListModel();
        int i;
        
        String tmp = (String)ComboBox_cat.getSelectedItem();
        
        if(tmp!=null)
        {
            switch(tmp) // Selon le résultat on remplit notre liste élément par élément
            {
                case "Politique" :  dlm = mw.getListPolModel();
                                    for(i = 0; i < dlm.getSize(); i++)
                                    {
                                        modelres.addElement(dlm.getElementAt(i));
                                    }
                                    setListResModel(modelres);
                                    break;
                                    
                case "International" :  dlm = mw.getListInterModel();
                                        for(i = 0; i < dlm.getSize(); i++)
                                        {
                                            modelres.addElement(dlm.getElementAt(i));
                                        }
                                        setListResModel(modelres);
                                        break;
                                        
                case "Sport" :  dlm = mw.getListSportModel();
                                for(i = 0; i < dlm.getSize(); i++)
                                {
                                    modelres.addElement(dlm.getElementAt(i));
                                }
                                setListResModel(modelres);
                                break;
                                
                case "Ragot" :  dlm = mw.getListRagotModel();
                                for(i = 0; i < dlm.getSize(); i++)
                                {
                                    modelres.addElement(dlm.getElementAt(i));
                                }
                                setListResModel(modelres);
                                break;
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

