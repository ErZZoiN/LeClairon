    private void list_resultatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_resultatMouseClicked
        if (evt.getClickCount() == 2)
        {
            String mot;
            mot = list_resultat.getSelectedValue();
            News news = new News();
            ArrayList<News> listeNews = new ArrayList<News>();
            listeNews = mw.getListeNews();
            
            int i = 0;
            while(!mot.equals(listeNews.get(i).getTitre())) // On cherche la news dans le vecteur
            {
                i++;
            }
            
            news = listeNews.get(i);
            
            MotResultatWindow mrw = new MotResultatWindow(news);
            mrw.setVisible(true);
        }
    }//GEN-LAST:event_list_resultatMouseClicked

